#program to calculate area and circumference of rectange
#area of rectange
print("Enter the area of rectangle:")
l=float(input())
b=float(input())
#formula a=l*b
area=l*b
print("Area of rectangle={}".format(area))

#circumference of rectangle
#formula perimeter=2l+2b
print("Enter the perimeter of rectangle")
l=float(input())
b=float(input())
perimeter=2*l+2*b
print("Area of perimeter of rectangle={}".format(perimeter))
